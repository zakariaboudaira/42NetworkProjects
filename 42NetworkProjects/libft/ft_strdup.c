#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	int		len;
	char	*copy;

	len = ft_strlen(str);
	copy = (char *)malloc(len * sizeof(char) + 1);
	if (copy == NULL)
	{
		return (0);
	}
	i = 0;
	while (str[i])
	{
		copy[i] = str[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
